// Examples of different identifier naming conventions in JavaScript

var name = "Malleswari";               // simple lowercase identifier

var firstName = "Malleswari";          // camelCase
var motherMaidenName = "Batchu";      // camelCase (multi-word)

var first_name = "Malleswari";         // snake_case
var last_name = "Batchu";              // snake_case (multi-word)

var FirstName = "Malleswari";          // PascalCase
var MotherMaidenName = "Batchu";       // PascalCase (multi-word)

// Kebab-case is not a valid JavaScript identifier, so it cannot be used directly:
// var first-name = "Malleswari";

var firstName2 = "Malleswari";          // camelCase with number
var _firstName = "Malleswari";         // leading underscore, valid identifier
var $firstName = "Malleswari";         // leading dollar sign, valid identifier


